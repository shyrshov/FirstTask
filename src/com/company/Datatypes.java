public class Datatypes {
    public static void main(String[] args) {
        byte a = 127;
        short b = -202;
        int c = 1000000;
        long d = 1256664664;
        float e = 3.7f;
        double f = 5.13;
        boolean g = true;
        char h = 'A';
        System.out.println("Primitive data types values: " + a + ", "+ b + ", "+ c + ", "+ d + ", "+ e + ", "+ f + ", "+ g + ", "+ h);
    }
}